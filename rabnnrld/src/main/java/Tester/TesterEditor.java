    private String getInstruction(){
        String[] newDireccion= new String[]{"DIAGONALARDE","DIAGONALARIZ","DIAGONALABDE","DIAGONALABIZ", "ARRIBA", "ABAJO", "DERECHA", "IZQUIERDA"};
        String[] olddirection= new String[]{"NORESTE", "NOROESTE", "SURESTE", "SUROESTE","NORTE", "SUR","ESTE", "OESTE"};
        String secuencia = "";
        for(int i = 0; i < this.i; i++){
            String intruccion = direcciones[i] + ":" + posiciones[i] + "-" + retardo[i];
            secuencia += intruccion; 
            if(i<this.i -1){
                secuencia += "_";
            }
        }
        for(int i = 0; i< olddirection.length; i++){
            secuencia = secuencia.replaceAll(olddirection[i], newDireccion[i]);
        }
        return secuencia;
    }
